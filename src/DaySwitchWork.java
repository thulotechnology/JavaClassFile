public class DaySwitchWork {
    public static void main(String[] args) {
        int noOfDay = 2;

        switch (noOfDay){
            case 1:
                System.out.println("Sunday");
                break;

            case 2:
                System.out.println("Monday");
                break;

            case 3:
                System.out.println("Tuesday");
                break;

            default:
                System.out.println("Invaild Option");
        }
    }
}
